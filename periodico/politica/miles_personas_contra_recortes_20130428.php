         <h2 onmouseover="relacionadas()"> Miles de personas se manifiestan en Barcelona contra los recortes y el paro </h2>
         
         <p>Miles de personas, 160.000 según los sindicatos CC OO y UGT, se han manifestado este mediodía en el centro de Barcelona contra los recortes y el paro en una de las marchas del Primero de Mayo más multitudinarias de los últimos años. La jornada llega a Cataluña con el trasfondo de la tasa de paro que reveló la semana pasada la Encuesta de Población Activa, 902.300 desempleados, una cifra inédita en la comunidad. Los dos grandes sindicatos han llamado a la ciudadanía, y en especial a los parados, a manifestarse bajo el lema Se lo quieren cargar todo, en referencia a los recortes presupuestarios y la privatización de los servicios públicos. En esta ocasión, también la corrupción se ha colado en la protesta, con pancartas en las que se puede leer el lema 'Tu sobre, mi recorte'.</p>
         
         <p> En la cabecera de la manifestación de Barcelona, junto a los líderes catalanes de UGT, Josep Maria Alvarez, y CCOO, Joan Carles Gallego, se ha situado desde el primer momento el presidente de ERC y socio parlamentario de CiU, Oriol Junqueras, donde también se han sumado posteriormente el líder del PSC, Pere Navarro, y la coordinadora nacional de ICV, Dolors Camats. Álvarez y Gallego han augurado hoy nuevas protestas en Cataluña ante la conflictividad laboral y la situación de crisis, y han pedido al presidente catalán, Artur Mas, la misma "valentía" para frenar los recortes que con el "derecho a decidir". </p>
         
         <p> "El gobierno catalán está más obsesionado por el pacto nacional por el derecho a decidir que por el derecho a trabajar. Urge un plan por el empleo juvenil y para los parados de larga duración, pero CiU y ERC están paralizados por su obsesión por caminar hacia al independencia", ha afirmado el máximo responsable del PSC.</p>
         
         <p> Tras el paro el segundo gran tema caliente de la jornada es el bloqueo de convenios colectivos que si no se negocian de nuevo antes del 7 de julio amenaza las condiciones salariales y laborales de decenas de miles de trabajadores. Los sindicatos hablan de hasta 800.000 personas, una cifra que la patronal Fomento del Trabajo rebajó ayer a 350.000. En la marcha han participado sectores como el agroalimentario, la limpieza, los educadores de tiempo libre, el puerto de Barcelona o, de forma inédita, el metal y la construcción de forma conjunta. Entre los colectivos amenazados por despidos se han sumado a la marcha los trabajadores de Alstom Winds o representantes de los medios de la Corporación Catalana de Medios de Comunicación (TV-3 y Catalunya Ràdio). </p>
         
         <p> En la marcha de hoy también se han colado los minijobs, rechazados unánimemente desde que ayer la patronal Pimec, que representa a las pequeñas y medianas empresas, los defendió –inspirados en los contratos parciales de 400 euros creados en Alemania—como vía para reducir el empleo e intentar que aflore la economía sumergida. El presidente de Pimec, Josep González, admitió que estas modalidades contractuales a tiempo parcial pueden fomentar la precariedad, pero peor es una tasa de empleo del 27%, defendió. El secretario de Empleo y Relaciones Laborales del Gobierno catalán, Ramon Bonastre, ha rechazado esta mañana la posibilidad de implantar minijobs en Cataluña, precisamente con el argumento del riesgo de precarizar el mercado del trabajo. </p>
         
         
        
